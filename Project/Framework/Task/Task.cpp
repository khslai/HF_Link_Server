/**************************************
Task停止処理
***************************************/
void Task::Stop()
{
	state = State::Finished;
}

/**************************************
Task再開処理
***************************************/
void Task::Resume()
{
	state = State::Idle;
}

/**************************************
Task中断処理
***************************************/
void Task::Pause()
{
	state = State::Paused;
}

/**************************************
Taskコンストラクタ
***************************************/
bool Task::IsFinished()
{
	return state == State::Finished;
}
